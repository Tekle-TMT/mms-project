    private void jLabel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel4MouseClicked
        int id = 0;
        String fname = "";
        String lname = "";
        int age = 0;
        String gender = "";
        String address = "";
        String email = "";
        String phone = "";
        String date = "";
        try {
            String key = tfsearch.getText();
            Connection conn = (Connection) DBconnection.connectDB();
            PreparedStatement st = (PreparedStatement) conn.prepareStatement(
                    "SELECT * FROM stuffs WHERE id=? or fname=? or lname=? or age=?  or gender=? or address=? or email=? or phone =? or date=?");
            st.setString(1, key);
            st.setString(2, key);
            st.setString(3, key);
            st.setString(4, key);
            st.setString(5, key);
            st.setString(6, key);
            st.setString(7, key);
            st.setString(8, key);
            st.setString(9, key);

            ResultSet rs = st.executeQuery();
            clearTable();
            for (int i = 0; i < 10; i++) {
                if (rs.next()) {
                    id = rs.getInt(1);
                    fname = rs.getString(2);
                    lname = rs.getString(3);
                    age = rs.getInt(4);
                    gender = rs.getString(5);
                    address = rs.getString(6);
                    email = rs.getString(7);
                    phone = rs.getString(8);
                    date = rs.getString(9);

                    table1.setValueAt(id, i, 0);
                    table1.setValueAt(fname, i, 1);
                    table1.setValueAt(lname, i, 2);
                    table1.setValueAt(age, i, 3);
                    table1.setValueAt(gender, i, 4);
                    table1.setValueAt(address, i, 5);
                    table1.setValueAt(email, i, 6);
                    table1.setValueAt(phone, i, 7);
                    table1.setValueAt(date, i, 8);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Visitors.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jLabel4MouseClicked

