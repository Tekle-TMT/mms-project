    private void btndeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btndeleteMouseClicked
        int y = table1.getSelectedRow();
        if (table1.isRowSelected(y)) {
            int t = JOptionPane.showConfirmDialog(this, "you sure?", "confirm.", JOptionPane.YES_NO_OPTION);
            if (t == JOptionPane.YES_OPTION) {
                table1.setValueAt(null, y, 0);
                table1.setValueAt(null, y, 1);
                table1.setValueAt(null, y, 2);
                table1.setValueAt(null, y, 3);
                table1.setValueAt(null, y, 4);
                table1.setValueAt(null, y, 5);
                table1.setValueAt(null, y, 6);
                table1.setValueAt(null, y, 7);
                table1.setValueAt(null, y, 8);
            } else {
                table1.removeRowSelectionInterval(y, 0);
            }
        } else {
            JOptionPane.showMessageDialog(this, "select row to delete.", "update", JOptionPane.OK_OPTION);

        }

    }//GEN-LAST:event_btndeleteMouseClicked

