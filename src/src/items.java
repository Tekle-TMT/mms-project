    private void itemsTable(String id, String name, String desc, String date) {
        try {
            Connection connect = (Connection) DBconnection.connectDB();
            PreparedStatement st = (PreparedStatement) connect.prepareStatement("INSERT INTO items VALUES (?,?,?,?)");
            st.setString(1, id);
            st.setString(2, name);
            st.setString(3, desc);
            st.setString(4, date);

            int x = st.executeUpdate();
            JOptionPane.showMessageDialog(this, "item added successfully");
            iid.setText("");
            iname.setText("");
            idate.setText("");
            ides.setText("");

            connect.close();
            displayTable();
        } catch (SQLException ex) {
            Logger.getLogger(Visitors.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void displayTable() {
        int id = 0;
        String name = "";
        String desc = "";
        String date = "";

        try {
            Connection conn = (Connection) DBconnection.connectDB();
            PreparedStatement add = (PreparedStatement) conn.prepareStatement("SELECT * FROM items");
            ResultSet rs = add.executeQuery();
//            table1.setModel(DbUtils.resultSetToTableModel(rs));
            int no_row = table1.getRowCount();
            for (int i = 0; i < no_row; i++) {
                if (rs.next()) {
                    id = rs.getInt(1);
                    name = rs.getString(2);
                    desc = rs.getString(3);
                    date = rs.getString(4);

                    table1.setValueAt(id, i, 0);
                    table1.setValueAt(name, i, 1);
                    table1.setValueAt(desc, i, 2);
                    table1.setValueAt(date, i, 3);

                }
            }
            conn.close();

        } catch (SQLException ex) {
            Logger.getLogger(Visitors.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void deleteItem(String y) {
        int b = JOptionPane.showConfirmDialog(this, "confirm?", "", JOptionPane.YES_NO_OPTION);
        if (b == JOptionPane.YES_OPTION) {
            try {
                Connection conn = (Connection) DBconnection.connectDB();
                PreparedStatement delete = (PreparedStatement) conn.prepareStatement("DELETE FROM items WHERE id=?");
                delete.setInt(1, Integer.parseInt(y));
                int x = delete.executeUpdate();
                conn.close();
                clearTable();
                displayTable();
            } catch (SQLException ex) {
                Logger.getLogger(Visitors.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    private void clearTable() {
        int no = table1.getRowCount();
        for (int i = 0; i < no; i++) {
            table1.setValueAt(null, i, 0);
            table1.setValueAt(null, i, 1);
            table1.setValueAt(null, i, 2);
            table1.setValueAt(null, i, 3);

        }
    }

    private void updateItems(String preId, int id, String name, String desc, String date) {
        try {
            Connection conn = (Connection) DBconnection.connectDB();
            PreparedStatement update = (PreparedStatement) conn.prepareStatement("UPDATE items SET id=?,item_name=?,item_desc=?,date=? WHERE id=? ");
            update.setInt(1, id);
            update.setString(2, name);
            update.setString(3, desc);
            update.setString(4, date);
            update.setString(5, preId);

            int y = update.executeUpdate();
            JOptionPane.showMessageDialog(this, "Item is updated ");
            updialog.setVisible(false);
            conn.close();

        } catch (SQLException ex) {
            Logger.getLogger(Visitors.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
