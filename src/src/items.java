    private void searchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchMouseClicked
        int id = 0;
        String name = "";
        String desc = "";
        String date = "";

        try {
            String key = tfsearch.getText();
            Connection conn = (Connection) DBconnection.connectDB();
            PreparedStatement st = (PreparedStatement) conn.prepareStatement(
                    "SELECT * FROM items WHERE id=? or item_name=? or item_desc=? or date=?");
            st.setString(1, key);
            st.setString(2, key);
            st.setString(3, key);
            st.setString(4, key);

            ResultSet rs = st.executeQuery();
            clearTable();
            for (int i = 0; i < 10; i++) {
                if (rs.next()) {
                    id = rs.getInt(1);
                    name = rs.getString(2);
                    desc = rs.getString(3);
                    date = rs.getString(4);

                    table1.setValueAt(id, i, 0);
                    table1.setValueAt(name, i, 1);
                    table1.setValueAt(desc, i, 2);
                    table1.setValueAt(date, i, 3);

                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Visitors.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_searchMouseClicked


